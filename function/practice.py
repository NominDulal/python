# Write a function called check_even_odd(num) that:
# Takes an integer as input.
# Prints "Even" if the number is even, otherwise prints "Odd".

def check_even_odd(num):
    if num%2==0:
        return "even"
    else:
        return "odd"
print(check_even_odd(4))    

